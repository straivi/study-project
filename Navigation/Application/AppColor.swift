import UIKit

extension UIColor {
    enum AppColor {
        static let accentColor = UIColor(named: "accent_color")
    }
    
    enum Subview {
        static let background = UIColor(named: "backgroung_color")
    }

    enum text {
        static let secondary = UIColor(named: "text_secondary")
    }

    enum button {
        static let primary = UIColor(named: "button_primary")
    }
}
