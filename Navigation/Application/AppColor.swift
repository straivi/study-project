import UIKit

extension UIColor {
    enum AppColor {
        static let accentColor = UIColor(named: "accent_color")
    }
    
    enum Subview {
        static let viewBackground = UIColor(named: "background_color")

        static let textSecondary = UIColor(named: "text_secondary")

        static let buttonPrimary = UIColor(named: "button_primary")
    }
}
